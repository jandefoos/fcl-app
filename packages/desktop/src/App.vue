<template>
  <IonApp>
    <IonSplitPane content-id="main-content">
      <ion-menu content-id="main-content" type="overlay">
        <ion-content>
          <ion-list id="inbox-list">
            <ion-list-header>fcl</ion-list-header>
            <ion-note
              >OBS Remote Control
              <span
                :class="'connection-state ' + socketState.connectionState"
              ></span
            ></ion-note>

            <ion-menu-toggle
              auto-hide="false"
              v-for="(p, i) in appPages"
              :key="i"
            >
              <ion-item
                @click="selectedIndex = i"
                router-direction="root"
                :router-link="p.url"
                lines="none"
                detail="false"
                class="hydrated"
                :class="{ selected: selectedIndex === i }"
                v-if="p.show"
              >
                <ion-icon
                  slot="start"
                  :ios="p.iosIcon"
                  :md="p.mdIcon"
                ></ion-icon>
                <ion-label>{{ p.title }}</ion-label>
              </ion-item>
            </ion-menu-toggle>
          </ion-list>
        </ion-content>
      </ion-menu>
      <ion-router-outlet id="main-content"></ion-router-outlet>
    </IonSplitPane>
  </IonApp>
</template>

<script lang="ts">
import {
  IonApp,
  IonContent,
  IonIcon,
  IonItem,
  IonLabel,
  IonList,
  IonListHeader,
  IonMenu,
  IonMenuToggle,
  IonNote,
  IonRouterOutlet,
  IonSplitPane,
} from "@ionic/vue";
import { defineComponent, ref } from "vue";
import {
  syncOutline,
  syncSharp,
  desktopOutline,
  desktopSharp,
  footballOutline,
  footballSharp,
  settingsOutline,
  settingsSharp,
} from "ionicons/icons";
import {
  state as socketState,
} from "@kickertech/common/socket";

export default defineComponent({
  name: "App",
  components: {
    IonApp,
    IonContent,
    IonIcon,
    IonItem,
    IonLabel,
    IonList,
    IonListHeader,
    IonMenu,
    IonMenuToggle,
    IonNote,
    IonRouterOutlet,
    IonSplitPane,
  },
  setup() {
    const selectedIndex = ref(0);

    const appPages = [
      {
        title: "Clicker",
        url: "/clicker",
        iosIcon: footballOutline,
        mdIcon: footballSharp,
        show: true,
      },
      {
        title: "Overlay Score",
        url: "/overlay",
        iosIcon: desktopOutline,
        mdIcon: desktopSharp,
        show: true,
      },
      {
        title: "Overlay Stats",
        url: "/overlaystats",
        iosIcon: desktopOutline,
        mdIcon: desktopSharp,
        show: true,
      },
      {
        title: "Discovery",
        url: "/discovery",
        iosIcon: syncOutline,
        mdIcon: syncSharp,
        show: process.env.IS_ELECTRON ? true : false,
      },
      {
        title: "Settings",
        url: "/settings",
        iosIcon: settingsOutline,
        mdIcon: settingsSharp,
        show: true,
      },
    ];

    const path = window.location.pathname.split("/")[1];
    if (path !== undefined) {
      selectedIndex.value = appPages.findIndex(
        (page) => page.title.toLowerCase() === path.toLowerCase()
      );
    }

    (async () => {
      const { accessKey } = await window.ipcRenderer.invoke("settings:sync");
      socketState.url = `wss://127.0.0.1:9901?access_key=${accessKey}`;
    })();

    return {
      socketState,
      selectedIndex,
      appPages,
    };
  },
});
</script>

